#define _MACRO_ONLY

#include <m32rasm.inc>
#include <s_services.h>
		
/*
 *  スタートアップルーチン 
 */
	.text
	.align 4
	.globl start

start:
	.extern		kernel_start
	.extern		hardware_init_hook
	.extern		software_init_hook
	.extern 	reset

	/* pswの初期化 */
	ldi		r0, 0	
	mvtc	r0, psw

	/* 割込みスタックポインタの初期化 */
	lea		r15, __stack
	
	/* EITベースレジスタの設定 */
	lea		r0, reset
	mvtc	r0, cr5

	/* ハードウェア初期化ルーチンの起動 */
	lea		r0, hardware_init_hook
	beqz	r0, L1
	jl		r0

L1:
	/* .bssセクションのクリア */
	xor		r0, r0
	lea		r1, __sbss
	lea		r2, __ebss
	beq		r1, r2, L3
L2:
	st		r0, @-r2
	bne		r1, r2, L2

L3:
	/* .dataセクションの初期化 */
	lea		r1, __sidata
	lea		r2, __eidata
	lea		r3, __sdata

	addi	r3, -4
	beq		r1, r2, L5
L4:	ld		r0, @r1+
	st		r0, @+r3
	bne		r1, r2, L4

L5:
	/* ソフトウェア初期化ルーチンの起動 */
	lea		r0, software_init_hook
	beqz	r0, L6
	jl		r0

L6:
	/* 割込みマスクの設定 */
	ld24 	r0, ICUIMASK + 1
	ldi 	r1, 7				/* すべての割込み要求を受付け */
	stb 	r1, @r0 			

	/* カーネル起動 */
	bl		kernel_start

L7:	bra		L7
