/*
 *  TOSHIBA IDEでは#pragma inline記述にてインライン関数を定義する
 *  このためカーネル内部にのみ以下の処理を適用する．
 *  カーネル共通部のInlineおよびinlineの定義は全て無効とし，既知の
 *  インライン関数に対する#pragma inline定義をkernel_inline_defs.h
 *  に記載し，そのファイルを本箇所にてインクルードする．
 */

#ifndef _MACRO_ONLY
#include "kernel_inline_defs.h"
#endif	/* _MACRO_ONLY	*/

#define	Inline


/*
 *  TOSHIBA IDEでは２種類のインラインアセンブラ命令がある
 *  __asm	: レジスタ退避有り(低速だがレジスタ破壊がない)
 *  __ASM	: レジスタ退避無し(高速だがレジスタ破壊に注意)
 *  カーネル内部で使用することを前提に__AMSを採用する
 */
#ifndef __cplusplus			/* C++ には asm がある */
#define	asm		__ASM
#endif /* __cplusplus */

#define	Asm		__ASM

#endif /* _TOOL_DEFS_H_ */
